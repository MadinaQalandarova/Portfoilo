function getLang() {
  const sel = document.getElementById("langMenu");
  return (sel && sel.value) || "uz";
}

function t(key) {
  const L = getLang();
  const pack = i18n[L] || i18n.uz;
  return pack[key] !== undefined
    ? pack[key]
    : i18n.uz[key] !== undefined
      ? i18n.uz[key]
      : key;
}

function applyLang(lang) {
  const pack = i18n[lang];
  if (!pack) return;
  document.documentElement.lang =
    lang === "uz" ? "uz" : lang === "ru" ? "ru" : "en";
  localStorage.setItem("portfolioLang", lang);

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (pack[key] !== undefined) el.textContent = pack[key];
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html");
    if (pack[key] !== undefined) el.innerHTML = pack[key];
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (pack[key] !== undefined) el.placeholder = pack[key];
  });
}

const langMenu = document.getElementById("langMenu");
const savedLang = localStorage.getItem("portfolioLang");
if (savedLang && i18n[savedLang]) langMenu.value = savedLang;
applyLang(getLang());

langMenu.addEventListener("change", (e) => applyLang(e.target.value));

const themeBtn = document.getElementById("themeBtn");
themeBtn.addEventListener("click", () => {
  if (document.body.getAttribute("data-theme") === "light") {
    document.body.removeAttribute("data-theme");
    themeBtn.textContent = "🌞";
  } else {
    document.body.setAttribute("data-theme", "light");
    themeBtn.textContent = "🌙";
  }
});

// SCROLL REVEAL
const observer = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry, i) => {
      if (entry.isIntersecting) {
        setTimeout(() => {
          entry.target.classList.add("visible");
          // Animate skill bars
          entry.target.querySelectorAll(".skill-bar").forEach((bar) => {
            setTimeout(() => bar.classList.add("animate"), 200);
          });
        }, i * 80);
      }
    });
  },
  { threshold: 0.15 },
);
document.querySelectorAll(".reveal").forEach((el) => observer.observe(el));

// COUNTER
const counters = document.querySelectorAll("[data-target]");
const counterObserver = new IntersectionObserver(
  (entries) => {
    entries.forEach((entry) => {
      if (!entry.isIntersecting) return;
      const el = entry.target;
      const target = +el.dataset.target;
      const dur = 1800;
      const step = target / (dur / 16);
      let cur = 0;
      const timer = setInterval(() => {
        cur = Math.min(cur + step, target);
        el.textContent = Math.floor(cur);
        if (cur >= target) clearInterval(timer);
      }, 16);
      counterObserver.unobserve(el);
    });
  },
  { threshold: 0.5 },
);
counters.forEach((c) => counterObserver.observe(c));

// CONTACT FORM (Telegram Bot API)
document.getElementById("contactForm").addEventListener("submit", async (e) => {
  e.preventDefault();
  const btn = e.target.querySelector(".form-submit");
  const msg = document.getElementById("formMessage");
  const name = document.getElementById("senderName").value;
  const email = document.getElementById("senderEmail").value;
  const text = document.getElementById("msgText").value;

  btn.textContent = t("form_sending");
  btn.disabled = true;
  msg.className = "form-message";
  msg.style.display = "none";

  try {
    const TELEGRAM_BOT_TOKEN = "8461587456:AAFwVMvF9wbENcVnznYa8nKdZmAYmqNaX-M";
    const TELEGRAM_OWNER_ID = "5579883280";

    async function resolveChatId() {
      const updatesRes = await fetch(
        `https://api.telegram.org/bot${TELEGRAM_BOT_TOKEN}/getUpdates`,
      );
      const updatesData = await updatesRes.json();
      if (!updatesRes.ok || !updatesData.ok) {
        throw new Error(
          updatesData?.description || "getUpdates ishlamadi. Tokenni tekshiring.",
        );
      }

      const ownerIdNum = Number(TELEGRAM_OWNER_ID);
      const updates = updatesData.result || [];

      for (let i = updates.length - 1; i >= 0; i -= 1) {
        const item = updates[i];
        const msgObj = item.message || item.edited_message;
        if (!msgObj || !msgObj.from || !msgObj.chat) continue;
        if (msgObj.from.id === ownerIdNum) {
          return msgObj.chat.id;
        }
      }

      throw new Error(
        "Bot sizning chatni topa olmadi. Botga /start yuboring, keyin qayta urinib ko'ring.",
      );
    }

    const resolvedChatId = await resolveChatId();
    const telegramText = [
      "Yangi contact xabari!",
      "",
      `Ism: ${name}`,
      `Email: ${email}`,
      `Xabar: ${text}`,
    ].join("\n");

    const res = await fetch(
      `https://api.telegram.org/bot${TELEGRAM_BOT_TOKEN}/sendMessage`,
      {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          chat_id: resolvedChatId,
          text: telegramText,
        }),
      },
    );
    const data = await res.json();
    if (!res.ok || !data.ok) {
      throw new Error(data?.description || "Telegramga yuborishda xatolik.");
    }

    msg.textContent = t("form_ok_full");
    msg.className = "form-message success";
    msg.style.display = "block";
    e.target.reset();
  } catch (err) {
    msg.textContent =
      err.message || "Xabar yuborilmadi. Iltimos qayta urinib ko'ring.";
    msg.className = "form-message error";
    msg.style.display = "block";
  }
  btn.textContent = t("form_submit");
  btn.disabled = false;
});
